    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String name = txtName.getText();
        String contact = txtContact.getText();
        String history = txtHistory.getText();
        int age = 0;

        try {
            age = Integer.parseInt(txtAge.getText());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age!");
            return;
        }

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "INSERT INTO patients (id, name, age, contact, medical_history) VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, id);
            pstmt.setString(2, name);
            pstmt.setInt(3, age);
            pstmt.setString(4, contact);
            pstmt.setString(5, history);

            pstmt.executeUpdate();

            conn.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Patient Saved to Cloud Successfully!");

            txtId.setText("");
            txtName.setText("");
            txtAge.setText("");
            txtContact.setText("");
            txtHistory.setText("");

            loadTableData();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error Saving: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPatients.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a patient to delete!");
            return;
        }

        String idToDelete = tblPatients.getValueAt(selectedRow, 0).toString();

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete patient " + idToDelete + "?",
                "Confirm Delete",
                javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return; 
        }

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();
            String sql = "DELETE FROM patients WHERE id = ?";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, idToDelete);
            pstmt.executeUpdate();
            conn.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Patient Deleted Successfully!");

            loadTableData();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error Deleting: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String name = txtName.getText();
        String contact = txtContact.getText();
        String history = txtHistory.getText();
        int age = Integer.parseInt(txtAge.getText());

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            // SQL: Update the other fields WHERE the ID matches
            String sql = "UPDATE patients SET name=?, age=?, contact=?, medical_history=? WHERE id=?";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, name);
            pstmt.setInt(2, age);
            pstmt.setString(3, contact);
            pstmt.setString(4, history);
            pstmt.setString(5, id);

            pstmt.executeUpdate();
            conn.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Patient Updated Successfully!");
            loadTableData();

            txtId.setEditable(true);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error Updating: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

