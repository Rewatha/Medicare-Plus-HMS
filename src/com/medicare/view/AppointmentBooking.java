    private void cmbDNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbDNameActionPerformed
        // TODO add your handling code here:
        if (cmbDName.getSelectedItem() == null) {
            return;
        }

        String selectedDoc = cmbDName.getSelectedItem().toString();

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();
            // Find the specialty for this specific doctor
            String sql = "SELECT specialty FROM doctors WHERE name=?";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, selectedDoc);

            java.sql.ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                lblSpecialty.setText(rs.getString("specialty"));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cmbDNameActionPerformed

    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        String pName = txtPName.getText();
        String dName = cmbDName.getSelectedItem().toString();
        String specialty = lblSpecialty.getText();
        String date = txtDate.getText();

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "INSERT INTO appointments (patient_name, doctor_name, doctor_specialty, appointment_date) VALUES (?, ?, ?, ?)";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, pName);
            pstmt.setString(2, dName);
            pstmt.setString(3, specialty);
            pstmt.setString(4, date);

            pstmt.executeUpdate();
            sendNotification(pName, "System Alert: Your appointment with " + dName + " is confirmed for " + date);

            sendNotification(dName, "System Alert: New appointment booked with patient " + pName + " on " + date);
            conn.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Appointment Booked!");
            loadTableData(); // Refresh the table

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBookActionPerformed

