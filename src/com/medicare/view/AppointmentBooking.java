    private void loadDoctorCombo() {
        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();
            String sql = "SELECT name FROM doctors";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pstmt.executeQuery();

            cmbDName.removeAllItems();

            while (rs.next()) {
                cmbDName.addItem(rs.getString("name"));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadTableData() {
        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "SELECT * FROM appointments";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pstmt.executeQuery();

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblBooking.getModel();
            model.setRowCount(0); 
            while (rs.next()) {
                String id = rs.getString("id");
                String pName = rs.getString("patient_name");
                String dName = rs.getString("doctor_name");
                String date = rs.getString("appointment_date");
                String status = rs.getString("status");

                model.addRow(new Object[]{id, pName, dName, date, status});
            }
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void sendNotification(String name, String message) {
        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "INSERT INTO notifications (recipient_name, message) VALUES (?, ?)";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, name);
            pstmt.setString(2, message);

            pstmt.executeUpdate();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

