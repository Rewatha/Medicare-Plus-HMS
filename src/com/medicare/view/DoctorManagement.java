    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String name = txtName.getText();
        String schedule = txtSchedule.getText();

        String specialty = cmbSpecialty.getSelectedItem().toString();

        double fee = 0.0;

        try {
            fee = Double.parseDouble(txtFee.getText());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid Fee!");
            return;
        }

        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "INSERT INTO doctors (id, name, specialty, schedule, fee) VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, id);
            pstmt.setString(2, name);
            pstmt.setString(3, specialty);
            pstmt.setString(4, schedule);
            pstmt.setDouble(5, fee);

            pstmt.executeUpdate();
            conn.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Doctor Added Successfully!");

            txtId.setText("");
            txtName.setText("");
            txtSchedule.setText("");
            txtFee.setText("");
            cmbSpecialty.setSelectedIndex(0);

            loadTableData(); 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error Saving: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblDoctorsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDoctorsMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblDoctors.getSelectedRow();
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblDoctors.getModel();

        txtId.setText(model.getValueAt(selectedRow, 0).toString());
        txtName.setText(model.getValueAt(selectedRow, 1).toString());

        String specialty = model.getValueAt(selectedRow, 2).toString();
        cmbSpecialty.setSelectedItem(specialty);

        txtSchedule.setText(model.getValueAt(selectedRow, 3).toString());
        txtFee.setText(model.getValueAt(selectedRow, 4).toString());

        txtId.setEditable(false); // Lock ID
    }//GEN-LAST:event_tblDoctorsMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDoctors.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a doctor to delete!");
            return;
        }

        String id = tblDoctors.getValueAt(selectedRow, 0).toString();

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Delete Doctor " + id + "?");

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                java.sql.Connection conn = com.medicare.controller.DBConnection.connect();
                java.sql.PreparedStatement pstmt = conn.prepareStatement("DELETE FROM doctors WHERE id=?");
                pstmt.setString(1, id);
                pstmt.executeUpdate();
                conn.close();

                loadTableData(); // Refresh table
                javax.swing.JOptionPane.showMessageDialog(this, "Doctor Deleted!");

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();

            String sql = "UPDATE doctors SET name=?, specialty=?, schedule=?, fee=? WHERE id=?";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, txtName.getText());
            pstmt.setString(2, cmbSpecialty.getSelectedItem().toString());
            pstmt.setString(3, txtSchedule.getText());
            pstmt.setDouble(4, Double.parseDouble(txtFee.getText()));
            pstmt.setString(5, txtId.getText()); // ID is the condition (WHERE id=...)

            pstmt.executeUpdate();
            conn.close();

            loadTableData();
            javax.swing.JOptionPane.showMessageDialog(this, "Doctor Updated!");
            txtId.setEditable(true); 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

