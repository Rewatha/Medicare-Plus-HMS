    private void loadTableData() {
        try {
            java.sql.Connection conn = com.medicare.controller.DBConnection.connect();
            String sql = "SELECT * FROM doctors";
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pstmt.executeQuery();

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblDoctors.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                String id = rs.getString("id");
                String name = rs.getString("name");
                String specialty = rs.getString("specialty");
                String schedule = rs.getString("schedule");
                double fee = rs.getDouble("fee");

                model.addRow(new Object[]{id, name, specialty, schedule, fee});
            }
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

